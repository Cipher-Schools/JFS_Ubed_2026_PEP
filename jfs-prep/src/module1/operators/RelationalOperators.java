package module1.operators;

// Relational operators: >, <, ==, !=, >=, <=
public class RelationalOperators {
    public static void main(String[] args) {
        int x = 15;
        int y = 20;
        
        System.out.println("x = " + x + ", y = " + y);
        System.out.println("x > y: " + (x > y));
        System.out.println("x < y: " + (x < y));
        System.out.println("x == y: " + (x == y));
        System.out.println("x != y: " + (x != y));
        System.out.println("x >= y: " + (x >= y));
        System.out.println("x <= y: " + (x <= y));
    }
}
